import Foundation

extension UIColor {
    func darkBlue() -> UIColor {
        return UIColor.init(red: 0.4, green: 21.0, blue: 45.0, alpha: 1.0)
    }
    
    func greenDefault() -> UIColor {
        return UIColor.init(red: 85.0, green: 164.0, blue: 36.0, alpha: 1.0)
    }
    
    func lightGray() -> UIColor {
        return UIColor.init(red: 240.0, green: 240.0, blue: 240.0, alpha: 1.0)
    }
    
    func redColor() -> UIColor {
        return UIColor.init(red: 255.0, green: 47.0, blue: 45.0, alpha: 1.0)
    }
}
